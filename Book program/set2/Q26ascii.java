public class Q26ascii {
    public static void main(String args[])
    {
        for(char ch='A';ch<='Z';ch++)
        {
            System.out.print((int)ch+" " );
            
        }
        System.out.println();
        for(char ch='a';ch<='z';ch++)
        {
            System.out.print((int)ch+" " );
           
        }
        System.out.println();
        for(char ch='0';ch<='9';ch++)
        {
            System.out.print((int)ch +" ");
           
        }
    }
    
}
